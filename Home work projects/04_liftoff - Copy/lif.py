def main():
    # Countdown from 10 to 1
    for i in range(10, 0, -1):  # Starting from 10, stopping at 1
        print(i, end=" ")  # Print the number with a space, not a newline

    print("Liftoff!")  # Print Liftoff! after the countdown

# This provided line is required at the end of the Python file
if __name__ == '__main__':
    main()
